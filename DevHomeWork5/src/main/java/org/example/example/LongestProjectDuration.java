package org.example.example;

public class LongestProjectDuration {
    private String id;
    private int duration;

    public LongestProjectDuration(String id, int duration) {
        this.id = id;
        this.duration = duration;
    }

    public String getId() {
        return id;
    }

    public int getDuration() {
        return duration;
    }
}

